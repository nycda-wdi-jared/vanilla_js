//to declare a variable, type "var"
//after "var", choose an appropriate name for the variable
//after the appropriate name, type "="
//then set the value of that variable to a datatype
var name = "jared"
var num_one = 4;
var num_two = 6;
var bool = false;
var boolTwo = true;

//if you look in your google inspect, you will see these items in there
